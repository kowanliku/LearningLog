/* 修改程序清单8.4中的猜测程序，使其使用更智能的猜测策略。例如，程序最初猜50，让其询问用户该猜测值是大、小还是正确。
 * 如果该猜测值小，则令下一次猜测值为50和100的中值，也就是75。
 * 如果75大，则下一次猜测值为75和50的中值，等等。
 * 使用这种二分搜索(binary search)策略，起码如果用户没有欺骗，该程序很快会获得正确答案。
 */
#include <stdio.h>

int main()// 这种题目不请楚的题不想做！
{
    int guess = 0;
    printf("猜吧1-100:");
    scanf("%d", &guess);
    if(guess>75)
        guess = (75+50)/2;
    else if(guess<50)
        guess = 75;
    printf("%d", guess);
    return 0;
}
